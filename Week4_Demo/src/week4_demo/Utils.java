package week4_demo;

public class Utils {
    
    // Calculates the average of two values.
    double getAvrg(int a, int b) {
        return (a+b)/2;
    }
    
    // Calculates the sum of two values.
    double getSum(int a, int b) {
        return (a+b);
    }
    
    // Calculates the cube of a value.
    double getCube(int a) {
        return (a*a*a);
    }
    
}
